// rules for declaring variables

// can't start with number
// 1man (invalid)
// man1 (valid)


// _ and $ are allowed
// first_name (valid)
// _firstName (valid)
// $firstName (valid)
// first$name (valid)


// can't use spaces
// first name (invalid)

// convention
// start with small letter and use camelCase

// my_name -> snake case
// myName -> camelCase
